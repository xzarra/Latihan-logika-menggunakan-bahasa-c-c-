#include <stdio.h>

int main (){
    int a; // integer adalah tipe data yang berguna untuk menampung angka yang bulat
    char b; // character adalah tipe data yang berguna untuk menambahkan 1 huruf atau 1 karakter
    float c; // float adalah tipe data yang berguna untuk menampung angka pecahan

    a = 20; 
    b = "A";
    c = 1.337;

    printf("%i\n",a);//inisialisasi integer bisa %i atau %d
    printf("%c\n",b);//inisialisasi character adalah %c
    printf("%f\n",c);//inisialisasi float adalah %f

    return 0;
}